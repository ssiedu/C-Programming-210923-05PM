#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n Ceil function : %.2f",ceil(3.1));
	printf("\n ceil function : %.2f",ceil(3.8));
	
	printf("\n floor function : %.2f",floor(3.1));
	printf("\n floor function : %.2f",floor(3.7));
	
	printf("\n sqrt function : %.2f",sqrt(81));
	printf("\n sqrt function : %.2f",sqrt(7));
	
	printf("\n pow function : %.2f",pow(3,4));
	printf("\n pow function : %.2f",pow(2.1,3.1));
	
	printf("\n abs function : %d",abs(-15));
	getch();
}
